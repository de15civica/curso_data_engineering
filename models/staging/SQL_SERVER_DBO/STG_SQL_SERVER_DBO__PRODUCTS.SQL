WITH SRC_PRODUCTS AS (
    SELECT * 
    FROM {{ source('SQL_SERVER_DBO', 'PRODUCTS') }}
    ),

RENAMED_PRODUCTS AS (
    SELECT
        PRODUCT_ID,
        PRICE AS PRICE_USD,
        NAME AS PLANT_NAME,
        _FIVETRAN_DELETED AS IS_ROW_DELETED,
        CONVERT_TIMEZONE('UTC', _FIVETRAN_SYNCED) AS DATETIME_ROW_LOADED
    FROM SRC_PRODUCTS
    )

SELECT * FROM RENAMED_PRODUCTS